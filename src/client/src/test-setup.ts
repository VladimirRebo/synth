// jsdom doesn't implement window.matchMedia — stub it so useTheme's initial-theme detection
// (which reads prefers-color-scheme) doesn't throw in tests. Real browsers all support it.
if (typeof window !== 'undefined' && !window.matchMedia) {
  window.matchMedia = (query: string) =>
    ({
      matches: false,
      media: query,
      onchange: null,
      addListener: () => {},
      removeListener: () => {},
      addEventListener: () => {},
      removeEventListener: () => {},
      dispatchEvent: () => false,
    }) as MediaQueryList
}
